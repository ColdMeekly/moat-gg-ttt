local data = fixes[game.GetMap()]
if (not data) then
    return
end

if (data.CreationIDs) then
    local eids = {}
    for _, v in pairs(data.CreationIDs) do
        eids[v] = true
    end
    hook.Add("OnEntityCreated", "Fix Map", function(e)
        timer.Simple(0, function()
            if (IsValid(e) and eids[e:MapCreationID()]) then
                e:Remove()
            end
        end)
    end)
end
if (data.Spawns) then
    hook.Add("TTTPrepareRound", "Fix Map", function()
        for _, pos in pairs(data.Spawns) do
            local e = ents.Create "info_player_deathmatch"
            e:SetPos(pos)
            e:Spawn()
        end
    end)
end